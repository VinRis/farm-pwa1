// Service Worker for PWA
const CACHE_NAME = 'agriflow-v2';
const urlsToCache = [
  '/farm-pwa1/',
  '/farm-pwa1/index.html',
  '/farm-pwa1/dairy.html',
  '/farm-pwa1/poultry.html',
  '/farm-pwa1/settings.html',
  '/farm-pwa1/master-report.html',
  '/farm-pwa1/css/style.css',
  '/farm-pwa1/css/dark-mode.css',
  '/farm-pwa1/js/app.js',
  '/farm-pwa1/js/charts.js',
  '/farm-pwa1/js/firebase.js',
  '/farm-pwa1/manifest.json',
  'https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.4.0/css/all.min.css',
  'https://fonts.googleapis.com/css2?family=Poppins:wght@300;400;500;600;700&display=swap',
  'https://cdn.jsdelivr.net/npm/chart.js'
];

// Install event with install prompt
self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        console.log('ðŸ“¦ Caching files for offline use');
        return cache.addAll(urlsToCache);
      })
      .then(() => {
        console.log('âœ… Service Worker installed');
        return self.skipWaiting();
      })
  );
});

// Activate event
self.addEventListener('activate', event => {
  const cacheWhitelist = [CACHE_NAME];
  
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (cacheWhitelist.indexOf(cacheName) === -1) {
            console.log('ðŸ—‘ï¸ Deleting old cache:', cacheName);
            return caches.delete(cacheName);
          }
        })
      );
    }).then(() => {
      console.log('âœ… Service Worker activated');
      return self.clients.claim();
    })
  );
});

// Fetch event
self.addEventListener('fetch', event => {
  // Skip cross-origin requests
  if (!event.request.url.startsWith(self.location.origin)) {
    return;
  }
  
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        if (response) {
          return response;
        }
        
        return fetch(event.request).then(response => {
          if (!response || response.status !== 200 || response.type !== 'basic') {
            return response;
          }
          
          const responseToCache = response.clone();
          
          caches.open(CACHE_NAME)
            .then(cache => {
              cache.put(event.request, responseToCache);
            });
          
          return response;
        });
      }).catch(() => {
        // If offline and request is for a page, return the home page
        if (event.request.headers.get('accept').includes('text/html')) {
          return caches.match('/farm-pwa1/index.html');
        }
      })
  );
});

// Listen for messages from the page
self.addEventListener('message', event => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    self.skipWaiting();
  }
});
